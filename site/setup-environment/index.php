<h1 id="setup-environment-for-developer">Setup Environment For Developer</h1>
<p>Environment: <code>MacOS</code></p>
<p>Ruby: <code>1.9.3-p194</code></p>
<h2 id="core-installations">Core installations</h2>
<p>Create file <code>.ipayment</code> at root directory, and add this to <code>.iPayment</code></p>
<pre><code>export ARCHFLAGS="-arch x86_64"
export PATH=/usr/local/bin:$PATH
</code></pre>
<p>Edit file <code>.bashrc</code></p>
<pre><code>source ~/.iPayment
</code></pre>
<p>Reload <code>.bashrc</code>    </p>
<pre><code class="sh">$ source ~/.bashrc
</code></pre>

<h2 id="install-working-environment">Install Working Environment</h2>
<ul>
<li>Install <code>brew</code> on <code>terminal</code></li>
</ul>
<pre><code class="sh">$ ruby -e &quot;$(curl -fsSL https://raw.github.com/mxcl/homebrew/go)&quot;

$ brew doctor

$ brew tap homebrew/dupes
</code></pre>

<ul>
<li>Install library with <code>brew</code></li>
</ul>
<pre><code class="sh">$ brew install bash curl wget git

$ brew install apple-gcc42

$ brew install autoconf automake libtool pkg-config openssl readline libyaml sqlite libxml2 libxslt libksba

$ brew install geos proj gdal

$ brew install imagemagick
</code></pre>

<h2 id="install-ruby">Install Ruby</h2>
<pre><code class="sh">$ wget https://cache.ruby-lang.org/pub/ruby/1.9/ruby-1.9.3-p194.zip

$ tar -zxvf ruby-1.9.3-p194.zip

$ cd ruby-1.9.3-p194.zip

$ ./configure

$ make

$ sudo make install
</code></pre>

<p>By default, this will install Ruby into <code>/usr/local</code>. To change, pass the <code>--prefix=DIR</code> option to the <code>./configure</code> script.
Reference: <a href="https://www.ruby-lang.org/en/documentation/installation/">Installation Ruby from source</a></p>
<h2 id="install-postgresql-via-postgis">Install PostgreSQL via PostGIS</h2>
<pre><code class="sh">$ brew install postgis
</code></pre>

<h2 id="install-mongodb">Install MongoDB</h2>
<pre><code class="sh">$ brew install mongodb
</code></pre>

<h2 id="install-redis">Install Redis</h2>
<pre><code class="sh">$ brew install redis
</code></pre>

<p>Reference: <a href="http://phab.intranet.pirq.com/w/developer_workstation_setup/">Developer Workstation Setup</a></p></div>
